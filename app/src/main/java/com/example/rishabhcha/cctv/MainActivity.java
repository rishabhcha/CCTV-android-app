    private void uploadImage(String mImagePath) {

        mRef = FirebaseDatabase.getInstance().getReference().child("Images");

        uri = Uri.fromFile(new File(mImagePath));
        String compressedImagePath = compressImage(uri.toString());
        uri = Uri.fromFile(new File(compressedImagePath));

        StorageReference filepath = mStorage.child("Images").child(uri.getLastPathSegment());
        filepath.putFile(uri).addOnSuccessListener(new OnSuccessListener<UploadTask.TaskSnapshot>() {
            @Override
            public void onSuccess(UploadTask.TaskSnapshot taskSnapshot) {

                final Uri downloadUrl = taskSnapshot.getDownloadUrl();

                //getting location--------------------------------------------
                gps = new TrackGPS(MainActivity.this);


                if(gps.canGetLocation()){
                    longitude = gps.getLongitude();
                    latitude = gps .getLatitude();

                    DatabaseReference newImageRef = mRef.push();
                    newImageRef.child("imageUrl").setValue(downloadUrl.toString());
                    newImageRef.child("latitude").setValue(Double.toString(latitude));
                    newImageRef.child("longitude").setValue(Double.toString(longitude));

                    new RetrieveFeedTask().execute();
                    new RetrieveFeedTask().execute();
                }
                else
                {

                    gps.showSettingsAlert();
                }
                //---------------------------------------------------------------
            }
        }).addOnFailureListener(new OnFailureListener() {
            @Override
            public void onFailure(@NonNull Exception e) {

                Toast.makeText(MainActivity.this, "Failed to upload. Please try again.", Toast.LENGTH_SHORT).show();

            }
        });


    }

